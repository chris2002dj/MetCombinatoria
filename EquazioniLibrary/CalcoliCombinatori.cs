using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace EquazioniLibrary
{
    public class CalcoliCombinatori
    {

        public static long fattoriale01(int n1)
        {
            long fattoriale = 1;

            for (int i = 1; i <= fattoriale; i++)
            {
                fattoriale = fattoriale * i;
            }

            return fattoriale;
        }
    }
